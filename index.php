                <br>
                <p><b>Korespondents: Ineta, kāpēc Jūs nekad neesat runājusi par savu nopietno slimību? Pēdējā laikā Jūs esat pazudusi no ziņu telpas un praktiski dzīvojāt izolācijā, nevienu neuzrunājāt.
 </b></p>

                <p><b>Ineta Radēviča:</b> Es uzskatu, ka tā ir mana personīgā darīšana un man nevajadzētu apgrūtināt citus ar savām problēmām. Turklāt es domāju, ka problēma nav tik kritiska, lai es būtu nopietni noraizējusies. Tāpēc es nolēmu, ka šo jautājumu neiztirzāšu publiski.
                </p>
                <br>
                <p><b>Korespondents: "Bet tagad Jūs esat nolēmusi ar to padalīties?"</b></p>

                <p><b>Ineta Radēviča: </b> Tagad es esmu tā izlēmusi, jā. Bet tā ir ne tik daudz vēlme pastāstīt par savu slimību, bet gan vēlme palīdzēt cilvēkiem, kurus, tāpat kā mani, parazīti saēd no iekšpuses – tā dēļ jebkurš var nomirt katru minūti. Neviens no mums nav aizsargāts no medicīniskām komplikācijām un tās var notikt ļoti pēkšņi. Šodien tu esi vesels cilvēks, bet rīt tu praktiski esi līķis, kuram nav iespēju izķepuroties. Un lielākā daļa ārstu tikai iekasēs no jums naudu, kamēr vien varēs, un mierīgi vēros, kā tu lēnām nomirsti.</p>
                <p><strong>Korespondents: "Ko Jūs ar to domājat?"</strong></p>
                <p><b>Ineta Radēviča: </b> Bija apjukuši, krita panikā. Viņi teica, ka tie ir parazītu kāpuri, kas dzīvu sagrauž manu labo nieri. Viņi deva man izvēli, operēt vēderu un iztīrt visu vai iziet konservatīvu ārstēšanas kursu slimnīcā. Pretējā gadījumā nieres vienkārši atteiksies un kāpuri izplatīsies visā ķermenī, un sāks iznīcināt citus orgānus. Tas novestu pie letāla iznākuma. Šāda operācija manā vecumā ir ļoti bīstams solis, tāpēc vienīgā alternatīva bija slimnīca. Es neteikšu, kādu cenu viņi man nosauca, taču tā pārsniedza visas saprāta robežas. Un, protams, viss ietu garām kasei. Oficiāli procedūras tika veiktas par centiem. Tagad es saprotu, ka tas viss bija maldināšana peļņas nolūkā un viņi to darīja uz manas veselības rēķina. Bet, ja pastāv reāli nāves draudi, loģika nestrādā.  </p>
                <p>Es piekritu nosauktajai summai, trīs nedēļas pavadīju slimnīcā kā parasts pacients un pēc tam katru dienu tur ierados vēl papildus. Ārstēšanas laikā sāpes vispirms mazinājās un pēc tam vispār izzuda.</p>
                <p><b>Korespondents: "Tātad tas tomēr palīdzēja?"</b></p>
                <p><b>Ineta Radēviča: </b> Ja nedomā par to, ka visu varēja atrisināt daudz vieglāk, tad jā - tas palīdzēja. Bet no viņu viedokļa skatoties, viņi vienkārši nopelnīja naudu uz pacienta rēķina. Turklāt palīdzība bija tik lokāla, ka veiksmīgi ietekmēja tikai viena veida parazītus. Kopumā bija trīs veidi, t.i. vismaz trīs šo briesmoņu perēkļi. Tad situācija attīstījās uz slikto pusi. Sākās sāpes sirdī, spiediens strauji lēkāja, lai gan tas nekad iepriekš nebija noticis, ļoti sāpēja galva, kājas sāka pampt.</p>
                <p>Dažreiz es nevarēju visu dienu piecelties no gultas. Es joprojām regulāri apmeklēju ārstus, tērēju daudz naudas dārgām zālēm, dzēru tablešu kaudzes. Un katru dienu arvien vairāk sabruku. Ja godīgi, vienā brīdī es domāju, ka es tā arī nomiršu. Es ne mirkli nejutu, ka man kļūst labāk.
                </p>
                <p><strong>Korespondents: “Tomēr jūs sēžat šeit, manā priekšā. Kā jūs izķepurojāties? "</strong></p>
                <p><b>Ineta Radēviča:</b> Palīdzēja nejaušība. Ciemos ieradās vecs draugs, kurš tagad strādā Veselības ministrijā. Viņš redzēja manu stāvokli, sāka apšaubīt ārstu teikto. Būtu muļķīgi visu noliegt, tāpēc es viņam visu izstāstīju, kā ir. Viņš mani izglāba. Pareizāk sakot, sākumā viņš ļoti sadusmojās un sāka tincināt manu ārstu vārdus. Tad viņš veica pāris zvanus un tajā pašā dienā viņi man atnesa jaunas zāles pret parazītiem, kas tika izstrādātas Veselības ministrijā. Toreiz es gandrīz atteicos, jo viņš pat nepaņēma no manis naudu un es pirmo reizi dzīvē redzēju šīs zāles. Bet, no otras puses, nebija ko zaudēt, tāpēc nolēmu pamēģināt.</p>
                <p>Jau nākamajā rītā pirmo reizi piecēlos no gultas 15 minūtes pēc pamošanās, nevis pēc 3-4 stundām. Un pēc 12-14 dienām es sapratu, ka sāpes sākušas mazināties. Pamazām pazuda arī komplikācijas un citi simptomi. Kā man paskaidroja ārsti, kurus ieteica draugs, zāles iznīcināja parazītus un apturēja to spēju vairoties. Jums nav ne mazākās nojausmas, kas no manis nāca ārā pirmajās dienās pēc tam, kad es sāku lietot šīs jaunās zāles. Tas bija gluži kā šausmu filmā. Jebkurā gadījumā, jau pēc 3 nedēļām es atkal jutos vesela. Ņemot vērā to, cik nopietns bija mans stāvoklis, tas ir vienkārši brīnums, ka man izdevās attīrīt savu ķermeni un atveseļoties tik īsā laikā.
                </p>
                <p><strong>Korespondents: “Tātad Jums ir izdevies attīrīt savu ķermeni no visiem parazītiem? Un kas notika ar ārstiem, kuri jūs "ārstēja" sākumā? " </strong></p>
                <p><b>Ineta Radēviča:</b> Varbūt tas izklausās neticami, it īpaši ņemot vērā manu stāvokli, bet jā - es pilnībā attīrīju savu ķermeni no parazītiem un atguvos. Ārstēšanas sākumā diagnostika parādīja, ka parazīti gandrīz pilnībā apsēduši zarnas, dzīvoja asinsvados, to izkārnījumi nokļuva asinīs, kas izraisīja iekaisumu un neatlaidīgi iznīcināja sirdi. Kopumā viss ķermenis bija pilns ar dažādiem tārpiem. Turklāt izrādījās, ka es jau sen biju ar viņiem inficējusies, taču dažādu apstākļu sakritību rezultātā tie pēkšņi sāka aktīvi vairoties, kas mani gandrīz nogalināja. Pēc labu ārstu teiktā, kuri novēro manu stāvokli kopš es atteicos no baltos halātos tērpto slepkavu pakalpojumiem, vismaz divi no trim Latvijas iedzīvotājiem ir inficēti ar parazītiem.</p>
                  <p>Runājot par ārstiem, kuri no manis izkrāpa naudu, viņi tika arestēti un šobrīd sniedz liecības. Izrādās, ka strādāja vesela organizēta dažādu iestāžu medicīnas darbinieku grupa, kas guva peļņu uz cilvēku rēķina. Tāpēc es gribu brīdināt cilvēkus būt ļoti uzmanīgiem. Ja šādi viņi izturējās pret pazīstamu cilvēku, tad man ir bail domāt, ko viņi dara ar tiem, kuriem nav iespēju sevi aizstāvēt pret ārstu nelietībām. </p>
        <p>Parasti katrs pats var diagnosticēt. Ja uz ādas aug papilomas, no mutes nāk nepatīkama smaka, bieži sāp galva, jūs bieži esat noguris, no rīta ir grūti piecelties no gultas - es dodu 90%, ka esat inficēts ar parazītiem. Sirdslēkmes, insults, vēzis, sepse, diabēts, hipertensija, artrīts, osteoartrīts - gandrīz jebkuru slimību var izraisīt tārpi vai amēbas jūsu ķermenī. Man tik tikko izdevās izvairīties no nieru vai koronārās mazspējas. Esmu piedzīvojis 5-6 mikroinfarktus parazītu dēļ, jo tie izdala toksīnus, kas nonāk sirdī. </p>
        <p><strong>Korespondents: "Vai varat pastāstīt mums par zālēm, kuras Jūs izārstēja?"</strong></p>
